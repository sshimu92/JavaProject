package beginnerJava;

public class SwappingNumber {

	public static void main(String[] args) {
		
		int a= 20;
		int b= 15;
		
		System.out.println("Before swapping the values are: "+ a +" "+ "&" +" "+ b);
		
		int s= a;
		 a= b;
		 b= s;
		 
		 System.out.println("After swapping the values are: "+ a +" "+ "&" +" "+ b);

	}

}
