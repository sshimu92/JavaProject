package Loops;

public class WhileLoop {

	public static void main(String[] args) {
		
		int i= 1;
		
		while(i<=10) {
			
			System.out.println(i +":"+ "Bangladesh");
			i++;
		}

		int j= 2;
		
		while(j<=10) {
			
			System.out.println(j);
			j+=2;
		}
		
		int k= 1;
		
		while(k<=10) {
			
			System.out.println(k);
			k+=2;
		}
	}

}
