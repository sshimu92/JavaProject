package BasicsJava;

public class DeclareEvenNumber {

	public static void main(String[] args) {
		
		 int num=4;
         
         if(num%2==0) {
       	  
       	  System.out.println(num+" is a even number");
        
         }else {
       	  
       	  System.out.println(num+" is a odd number");
         }

	}

}
